#include "../push_swap.h"

void	swap_a_bonus(t_stack *s)
{
	int	temp;

	if (s->i >= s->element_cnt - 1)
		return ;
	temp = s->a[s->i];
	s->a[s->i] = s->a[s->i + 1];
	s->a[s->i + 1] = temp;
}
